package br.com.zup;

public class Exercicio1 {

    public static void main(String[] args) {
	// 1 - Faça um programa que mostra na tela os números de 1 a 100.
        int contador = 1;

        while (contador <= 100) {
            System.out.println(contador);
            contador ++;

        }
    }
}
